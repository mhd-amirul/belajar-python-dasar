## Operator Assignment

x = 6
print('Nilai x     :',x)

x += 1 #x = x + 1
print('Nilai x + 1 :',x)

x -= 1 #x = x - 1
print('Nilai x - 1 :',x)

x *= 10 #x = x * 10
print('Nilai x * 10 :',x)

x /= 2 #x = x / 2
print('Nilai x / 2 :',x)

x %= 20 #x = x % 20
print('Nilai x % 20 :',x)

x //= 2 #x = x // 2
print('Nilai x // 2 :',x)

x **= 5 #x = x ** 5
print('Nilai x ** 5 :',x)

