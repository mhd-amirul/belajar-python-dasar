# String

data = "Ini adalah data string"
print(data)

## cara membuat string
# 1. menggunakan single quote 'data'
print('Ini adalah data string')

# 2. menggunakan double quote "data"
print("Ini adalah data string")

# menampilkan tanda quote di string
print("'Ini adalah data string'")
print('"Ini adalah data string"')

# mengubah tanda ' dan \ menjadi string
print("Hari ini hari jum'at")
# menggunakan \
print('Hari ini hari jum\'at')
print('c:\\User\\Document\\New folder')

# tab , newline, dan backspace
print('Bab1\tHukum Judi')
print('Baris 1\nBaris 2')
print('Hapus\b menjadi hapus')

#string raw
print('C:\\new folder')
print(r'C:\new folder')

# multiline
print('''
Nama  :
NIM   :
Kelas :
''')

