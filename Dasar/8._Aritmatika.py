# Operasi aritmatika (tambah (+), Kali(*), Bagi(/), Kurang(-), Modulus(%))

a = 10
b = 7
c = 34
d = 2

#Tambah
print("=========Tambah==========")
z = a + b
print(a ,"+",b,"=",z)

#Kurang
print("=========Kurang==========")
z = a - c
print(a ,"-",c,"=",z)

#Kali
print("=========Kali==========")
z = a * c
print(a ,"*",c,"=",z)

#Bagi
print("=========Bagi==========")
z = a / d
print(a ,"/",d,"=",z)

#Modulus
print("=========Modulus==========")
z = a % d
print(a ,"%",d,"=",z)

#Floor division
print("=========Floor division==========")
z = a // d
print(a ,"//",d,"=",z)

